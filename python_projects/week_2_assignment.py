# Creating an empty list
my_list = []
# .append() method to add elements one by one or the .extend() method to add multiple elements at once
my_list.append(10)
my_list.append(20)
my_list.append(30)
my_list.append(40)
# .insert() method to add a value at a specific position in the list
# .insert(index, value) inserts value at the specified index.
my_list.insert(1, 15)
# Extending the list
my_list.extend([50, 60, 70])
# .pop(index) method to remove the value at a specific position in the list by index
my_list.pop()
# .sort() method to sort the list in ascending order
my_list.sort()
# .index(value) method to find the position of the value()
my_list.index(30)
